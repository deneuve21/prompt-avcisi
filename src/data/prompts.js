// Helper function to get all prompts
export const getAllPrompts = () => {
  return {
    total: 100,
    categories: {
      footballers: promptDatabase.footballers.length,
      superheroes: promptDatabase.superheroes.length,
      socialmedia: promptDatabase.socialmedia.length,
      backgrounds: promptDatabase.backgrounds.length,
      artdesign: promptDatabase.artdesign.length
    },
    prompts: [
      ...promptDatabase.footballers,
      ...promptDatabase.superheroes,
      ...promptDatabase.socialmedia,
      ...promptDatabase.backgrounds,
      ...promptDatabase.artdesign
    ]
  };
};

// Helper function to get prompts by category
export const getPromptsByCategory = (category) => {
  return promptDatabase[category] || [];
};

// Helper function to search prompts
export const searchPrompts = (query) => {
  const allPrompts = getAllPrompts().prompts;
  return allPrompts.filter(prompt =>
    prompt.name.toLowerCase().includes(query.toLowerCase()) ||
    prompt.prompt.toLowerCase().includes(query.toLowerCase()) ||
    (prompt.metadata.tr_name && prompt.metadata.tr_name.toLowerCase().includes(query.toLowerCase()))
  );
};

// Helper function to get random prompt
export const getRandomPrompt = () => {
  const allPrompts = getAllPrompts().prompts;
  return allPrompts[Math.floor(Math.random() * allPrompts.length)];
};

